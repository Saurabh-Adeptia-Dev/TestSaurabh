package com.adeptia.ftp;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import java.io.IOException;
import java.util.concurrent.atomic.AtomicInteger;

/**
 * FTPConnectionManager
 * ?????????????????????
 * Manages a single remote file handler connection with:
 *
 *   1. Auto-reconnect     ? if connection times out or drops, transparently
 *                           reconnects before the next operation
 *   2. Keep-alive         ? sends FTP NOOP periodically to prevent idle timeout
 *   3. Retry logic        ? retries failed connections up to MAX_RETRIES times
 *   4. Health check       ? verifies connection is alive via NOOP before each op
 *   5. Connection counter ? global AtomicInteger tracks how many FTP connections
 *                           are open at any point in time (thread-safe)
 *
 * ?? Connection Counter ???????????????????????????????????????????????????
 *
 *   A static AtomicInteger (ACTIVE_CONNECTION_COUNT) is incremented on every
 *   successful connect() and decremented on every disconnect().
 *   It is thread-safe ? works correctly in both sequential and parallel modes.
 *
 *   Read the current count at any time:
 *     FTPConnectionManager.getActiveConnectionCount()   ? int
 *     FTPConnectionManager.getConnectionStats()         ? formatted String
 *
 *   The count is logged at every connect, disconnect, and reconnect so you
 *   can track the number of open connections in the Adeptia log.
 *
 * ?? Usage ????????????????????????????????????????????????????????????????
 *
 *   try (FTPConnectionManager mgr = new FTPConnectionManager(
 *           host, port, user, pass, useSSL, transferType, "SOURCE")) {
 *       mgr.connect();
 *       mgr.getConnection().readFile(...);
 *       mgr.getConnection().writeFile(...);
 *   } // auto-disconnects, counter decremented
 *
 *   // Check count at any time:
 *   int open = FTPConnectionManager.getActiveConnectionCount();
 */
public class FTPConnectionManager implements AutoCloseable {

    private static final Logger LOG = LoggerFactory.getLogger(FTPConnectionManager.class);

    // ?? Global Connection Counter ?????????????????????????????????????????
    // AtomicInteger ? safe for concurrent access from multiple threads
    // Incremented on successful connect, decremented on disconnect
    // Reconnects = decrement old + increment new = net 0 (correct)
    private static final AtomicInteger ACTIVE_CONNECTION_COUNT = new AtomicInteger(0);

    // ?? Total connections opened since JVM start (for diagnostics) ????????
    private static final AtomicInteger TOTAL_CONNECTIONS_OPENED = new AtomicInteger(0);
    private static final AtomicInteger TOTAL_RECONNECTS         = new AtomicInteger(0);

    // ?? Retry Config ??????????????????????????????????????????????????????
    private static final int MAX_RETRIES    = 3;
    private static final int RETRY_DELAY_MS = 2_000;

    // ?? Keep-Alive Config ?????????????????????????????????????????????????
    private static final int KEEPALIVE_INTERVAL_SEC     = 60;
    private static final int KEEPALIVE_REPLY_TIMEOUT_MS = 10_000;

    // ?? Protocol Detection ???????????????????????????????????????????????
    // Port 22 = SFTP (SSH), anything else = FTP/FTPS
    private static final int SFTP_PORT = 22;

    // ?? Instance Fields ???????????????????????????????????????????????????
    // handler field typed as IFileHandler -- requires both FtpRemoteHandler
    // and SftpRemoteHandler to implement IFileHandler
    private IFileHandler handler;
    private boolean          counted = false;
    private FTPAccountConfig config  = null;  // set when using FTPAccountConfig constructor

    private final String  host;
    private final int     port;
    private final String  username;
    private final String  password;
    private final boolean useSSL;
    private final String  transferType;
    private final String  role;
    private final boolean isSFTP;    // true when port == 22

    // ?? Constructor ???????????????????????????????????????????????????????

    /**
     * Constructor from FTPAccountConfig ? preferred approach.
     * Protocol is already resolved in FTPAccountConfig.
     *
     * @param config  Resolved FTPAccountConfig from Adeptia FTPAccount activity
     * @param role    "SOURCE", "TARGET", "SINGLE", or "LIST" ? for logging
     */
    public FTPConnectionManager(FTPAccountConfig config, String role) {
        this.host         = config.host;
        this.port         = config.port;
        this.username     = config.username;
        this.password     = config.password;
        this.useSSL       = config.useSSL;
        this.transferType = config.transferType;
        this.role         = role;
        this.isSFTP       = config.isSFTP;
        this.config       = config;

        LOG.info("[{}] Protocol: {} | host={}:{} | user={}",
                role, config.getProtocolName(), host, port, username);
    }

    /**
     * Constructor from raw parameters ? kept for backward compatibility
     * and for cases where FTPAccountConfig is not available.
     *
     * @param host          FTP server hostname or IP
     * @param port          FTP port (21 = FTP/FTPS, 22 = SFTP)
     * @param username      FTP/SFTP username
     * @param password      FTP/SFTP password
     * @param useSSL        true = FTPS | false = plain FTP (ignored for SFTP)
     * @param transferType  "Passive" or "Active" (FTP only)
     * @param role          "SOURCE", "TARGET", "SINGLE", or "LIST"
     */
    public FTPConnectionManager(String  host,     int    port,
                                String  username, String password,
                                boolean useSSL,   String transferType,
                                String  role) {
        this.host         = host;
        this.port         = port;
        this.username     = username;
        this.password     = password;
        this.useSSL       = useSSL;
        this.transferType = transferType;
        this.role         = role;
        this.isSFTP       = (port == SFTP_PORT);

        LOG.info("[{}] Protocol: {} | host={}:{} | user={}",
                role, isSFTP ? "SFTP" : (useSSL ? "FTPS" : "FTP"), host, port, username);
    }

    // ?????????????????????????????????????????????????????????????????????
    // Static ? Connection Counter API
    // ?????????????????????????????????????????????????????????????????????

    /**
     * Returns the number of FTP connections currently open.
     * Thread-safe ? reads AtomicInteger.
     *
     * @return number of active connections right now
     */
    public static int getActiveConnectionCount() {
        return ACTIVE_CONNECTION_COUNT.get();
    }

    /**
     * Returns total connections ever opened since JVM start.
     */
    public static int getTotalConnectionsOpened() {
        return TOTAL_CONNECTIONS_OPENED.get();
    }

    /**
     * Returns total number of auto-reconnects that have occurred.
     */
    public static int getTotalReconnects() {
        return TOTAL_RECONNECTS.get();
    }

    /**
     * Returns a formatted connection stats string ? useful for logging.
     *
     * Example output:
     *   "FTP Connections ? Active: 2 | TotalOpened: 5 | Reconnects: 1"
     */
    public static String getConnectionStats() {
        return String.format(
                "FTP Connections ? Active: %d | TotalOpened: %d | Reconnects: %d",
                ACTIVE_CONNECTION_COUNT.get(),
                TOTAL_CONNECTIONS_OPENED.get(),
                TOTAL_RECONNECTS.get());
    }

    /**
     * Resets all counters ? useful for testing or between batch runs.
     */
    public static void resetCounters() {
        ACTIVE_CONNECTION_COUNT.set(0);
        TOTAL_CONNECTIONS_OPENED.set(0);
        TOTAL_RECONNECTS.set(0);
        LOG.info("FTP connection counters reset.");
    }

    // ?????????????????????????????????????????????????????????????????????
    // connect()
    // ?????????????????????????????????????????????????????????????????????

    /**
     * Opens the initial FTP connection with retry logic.
     * Increments ACTIVE_CONNECTION_COUNT on success.
     *
     * @throws IOException if all retry attempts fail
     */
    public void connect() throws IOException {
        LOG.info("[{}] Opening FTP connection to [{}:{}]. {}",
                role, host, port, getConnectionStats());
        connectWithRetry("initial connect");
        applyKeepAlive();
    }

    // ?????????????????????????????????????????????????????????????????????
    // getConnection()
    // ?????????????????????????????????????????????????????????????????????

    /**
     * Returns a healthy IFileHandler, reconnecting transparently if needed.
     * Reconnect updates the connection counter correctly:
     *   decrement (old dead connection) + increment (new connection) = net 0
     *
     * @return  Connected and verified IFileHandler
     * @throws IOException if reconnect fails after MAX_RETRIES attempts
     */
    public IFileHandler getConnection() throws IOException {
        if (!isAlive()) {
            LOG.warn("[{}] Connection to [{}] is dead or timed out. Reconnecting... {}",
                    role, host, getConnectionStats());
            reconnect();
        }
        return handler;
    }

    // ?????????????????????????????????????????????????????????????????????
    // disconnect()
    // ?????????????????????????????????????????????????????????????????????

    /**
     * Gracefully disconnects and decrements ACTIVE_CONNECTION_COUNT.
     * Idempotent ? safe to call multiple times (guarded by 'counted' flag).
     */
    public void disconnect() {
        if (handler != null) {
            handler.disconnect();
            handler = null;
        }
        // Decrement counter only if this instance was counted as active
        if (counted) {
            int active = ACTIVE_CONNECTION_COUNT.decrementAndGet();
            counted = false;
            LOG.info("[{}] Disconnected from [{}]. {}",
                    role, host, getConnectionStats());
        }
    }

    // ?????????????????????????????????????????????????????????????????????
    // close() ? AutoCloseable
    // ?????????????????????????????????????????????????????????????????????

    @Override
    public void close() {
        disconnect();
    }

    // ?????????????????????????????????????????????????????????????????????
    // Private ? Health Check
    // ?????????????????????????????????????????????????????????????????????

    private boolean isAlive() {
        if (handler == null || !handler.isConnected()) {
            LOG.debug("[{}] isAlive ? handler null or socket closed.", role);
            return false;
        }
        try {
            boolean alive = handler.sendNoOp();
            if (!alive) {
                LOG.debug("[{}] isAlive ? NOOP negative reply.", role);
            }
            return alive;
        } catch (IOException e) {
            LOG.debug("[{}] isAlive ? NOOP threw: {}", role, e.getMessage());
            return false;
        }
    }

    // ?????????????????????????????????????????????????????????????????????
    // Private ? Reconnect
    // ?????????????????????????????????????????????????????????????????????

    /**
     * Closes the dead connection (decrements counter) and opens a fresh one.
     * Net effect on counter = 0 (decrement old + increment new).
     */
    private void reconnect() throws IOException {
        LOG.info("[{}] Reconnecting to [{}:{}]... {}", role, host, port, getConnectionStats());

        // Close dead connection and decrement counter
        if (handler != null) {
            try { handler.disconnect(); } catch (Exception e) {
                LOG.debug("[{}] Error closing dead connection (ignored): {}", role, e.getMessage());
            }
            handler = null;
        }
        if (counted) {
            ACTIVE_CONNECTION_COUNT.decrementAndGet();
            counted = false;
        }

        TOTAL_RECONNECTS.incrementAndGet();
        connectWithRetry("reconnect");
        applyKeepAlive();
        LOG.info("[{}] Reconnected to [{}]. {}", role, host, getConnectionStats());
    }


    // ?????????????????????????????????????????????????????????????????????
    // Private ? Build minimal FTPAccountConfig from raw params
    // ?????????????????????????????????????????????????????????????????????

    /**
     * Builds a minimal FTPAccountConfig from the raw constructor params.
     * Used as fallback when constructed without FTPAccountConfig.
     */
    private FTPAccountConfig buildConfig() {
        return FTPAccountConfig.fromRaw(host, port, username, password,
                useSSL, transferType);
    }

    // ?????????????????????????????????????????????????????????????????????
    // Private ? Connect with Retry
    // ?????????????????????????????????????????????????????????????????????

    /**
     * Attempts to connect up to MAX_RETRIES times.
     * Increments ACTIVE_CONNECTION_COUNT and TOTAL_CONNECTIONS_OPENED on success.
     */
    private void connectWithRetry(String context) throws IOException {
        IOException lastException = null;

        for (int attempt = 1; attempt <= MAX_RETRIES; attempt++) {
            try {
                LOG.info("[{}] {} attempt [{}/{}] to [{}:{}].",
                        role, context, attempt, MAX_RETRIES, host, port);

                // Protocol routing:
                //   SFTP (port 22) -> SftpRemoteHandler  (JSch direct)
                //   FTP / FTPS     -> FtpRemoteHandler   (Apache Commons Net)
                FTPAccountConfig cfg = (config != null)
                        ? config
                        : FTPAccountConfig.fromRawParams(
                        host, port, username, password, useSSL, transferType);

                if (cfg.isSFTP) {
                    handler = new SftpRemoteHandler(cfg);
                    LOG.info("[{}] Using SftpRemoteHandler for SFTP [{}:{}].",
                            role, host, port);
                } else {
                    handler = new FtpRemoteHandler(cfg);
                    LOG.info("[{}] Using FtpRemoteHandler for {} [{}:{}].",
                            role, cfg.useSSL ? "FTPS" : "FTP", host, port);
                }
                handler.connect();

                // ?? Success ? update counters ?????????????????????????????
                int active = ACTIVE_CONNECTION_COUNT.incrementAndGet();
                TOTAL_CONNECTIONS_OPENED.incrementAndGet();
                counted = true;

                LOG.info("[{}] {} succeeded on attempt [{}]. {}",
                        role, context, attempt, getConnectionStats());
                return;

            } catch (IOException e) {
                lastException = e;
                LOG.warn("[{}] {} attempt [{}/{}] failed: {}",
                        role, context, attempt, MAX_RETRIES, e.getMessage());

                if (handler != null) {
                    try { handler.disconnect(); } catch (Exception ignored) {}
                    handler = null;
                }

                if (attempt < MAX_RETRIES) {
                    LOG.info("[{}] Waiting {}ms before retry [{}/{}]...",
                            role, RETRY_DELAY_MS, attempt + 1, MAX_RETRIES);
                    try {
                        Thread.sleep(RETRY_DELAY_MS);
                    } catch (InterruptedException ie) {
                        Thread.currentThread().interrupt();
                        throw new IOException("Interrupted during retry wait.", ie);
                    }
                }
            }
        }

        throw new IOException(String.format(
                "[%s] %s failed after %d attempts to [%s:%d]. Last error: %s",
                role, context, MAX_RETRIES, host, port,
                lastException != null ? lastException.getMessage() : "unknown"));
    }

    // ?????????????????????????????????????????????????????????????????????
    // Private ? Keep-Alive
    // ?????????????????????????????????????????????????????????????????????

    private void applyKeepAlive() {
        if (handler != null) {
            handler.setKeepAlive(KEEPALIVE_INTERVAL_SEC, KEEPALIVE_REPLY_TIMEOUT_MS);
            LOG.debug("[{}] Keep-alive set. NOOP every {}s.", role, KEEPALIVE_INTERVAL_SEC);
        }
    }
}